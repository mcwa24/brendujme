const brandCountryMap = new Map<string, string>();

function registerCountry(slug: string, country: string | undefined) {
  const trimmed = country?.trim();
  if (trimmed) brandCountryMap.set(slug, trimmed);
}

for (const brand of [
  ...staticBrands,
  ...homeRetailBrands,
  ...techRetailBrands,
  ...beautyRetailBrands,
]) {
  registerCountry(brand.slug, brand.country);
}

for (const [slug, country] of Object.entries(SCRAPED_BRAND_COUNTRIES)) {
  registerCountry(slug, country);
}

/** Zemlja porekla za Bilbord slug; undefined samo za nepoznat slug. */
export function getBrandCountry(slug: string): string | undefined {
  return brandCountryMap.get(slug);
}

/** Svi poznati slug → zemlja parovi (za seed skripte). */
export function getAllBrandCountries(): ReadonlyMap<string, string> {
  return brandCountryMap;
}
