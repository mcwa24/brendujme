export function getNewsBySlug(slug: string): NewsArticle | undefined {
  return newsArticles.find((n) => n.slug === slug);
}

export function getFeaturedNews(): NewsArticle | undefined {
  return newsArticles.find((n) => n.featured) ?? newsArticles[0];
}

export function getLatestNews(limit = 6): NewsArticle[] {
  return [...newsArticles]
    .sort(
      (a, b) =>
        new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    )
    .slice(0, limit);
}

export function getNewsByBrand(slug: string, limit = 10): NewsArticle[] {
  return newsArticles
    .filter((n) => n.brandSlugs?.includes(slug))
    .sort(
      (a, b) =>
        new Date(b.publishedAt).getTime() - new Date(a.publishedAt).getTime()
    )
    .slice(0, limit);
}
